package com.lezo.iscript.font;

public class FontSimilarVector {
	private FontVector refer;
	private FontVector vector;
	private double score;

	public FontVector getRefer() {
		return refer;
	}

	public void setRefer(FontVector refer) {
		this.refer = refer;
	}

	public FontVector getVector() {
		return vector;
	}

	public void setVector(FontVector vector) {
		this.vector = vector;
	}

	public double getScore() {
		return score;
	}

	public void setScore(double score) {
		this.score = score;
	}

}
